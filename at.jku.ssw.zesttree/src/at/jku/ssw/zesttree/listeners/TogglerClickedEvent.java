package at.jku.ssw.zesttree.listeners;

import org.eclipse.draw2d.ActionEvent;

import at.jku.ssw.zesttree.widgets.ClickableGraphNode;

public class TogglerClickedEvent extends ActionEvent {

	private static final long serialVersionUID = 308350561797461972L;
	private ClickableGraphNode node;

	public TogglerClickedEvent(Object source, ClickableGraphNode node) {

		super(source);
		this.node = node;
	}

	public ClickableGraphNode getNode() {

		return node;
	}
}
